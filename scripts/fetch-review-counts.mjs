#!/usr/bin/env node
/**
 * Fetch per-thread comment counts for every giscus discussion in the configured
 * repo + category, and write them to src/data/review-counts.json. Run before
 * `vite build` so the bundle includes fresh counts.
 *
 * Falls back to an empty count map (build still succeeds) when:
 *   - no GITHUB_TOKEN is available (local dev),
 *   - the network call fails,
 *   - the GraphQL response is malformed.
 *
 * Environment:
 *   GITHUB_TOKEN        — required for fetch; provided automatically in GH Actions
 *   GISCUS_REPO_OWNER   — defaults to alxshtanko
 *   GISCUS_REPO_NAME    — defaults to policies-rfc
 *   GISCUS_CATEGORY_ID  — defaults to the General category on policies-rfc
 *   GISCUS_TERM_PREFIX  — defaults to "policy-review"
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_OWNER   = process.env.GISCUS_REPO_OWNER   ?? 'alxshtanko';
const REPO_NAME    = process.env.GISCUS_REPO_NAME    ?? 'policies-rfc';
const CATEGORY_ID  = process.env.GISCUS_CATEGORY_ID  ?? 'DIC_kwDOSc3lIM4C8_r1';
const TERM_PREFIX  = process.env.GISCUS_TERM_PREFIX  ?? 'policy-review';
const TOKEN        = process.env.GITHUB_TOKEN;

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_PATH  = resolve(__dirname, '..', 'src', 'data', 'review-counts.json');

function write(payload) {
  mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, JSON.stringify(payload, null, 2) + '\n');
}

async function main() {
  if (!TOKEN) {
    console.warn('[review-counts] No GITHUB_TOKEN — writing empty count map.');
    write({ counts: {}, fetchedAt: null });
    return;
  }

  const query = `
    query ReviewCounts($owner: String!, $name: String!, $categoryId: ID!) {
      repository(owner: $owner, name: $name) {
        discussions(first: 100, categoryId: $categoryId) {
          nodes {
            title
            comments { totalCount }
          }
        }
      }
    }
  `;

  let res;
  try {
    res = await fetch('https://api.github.com/graphql', {
      method:  'POST',
      headers: {
        Authorization:  `bearer ${TOKEN}`,
        'Content-Type': 'application/json',
        'User-Agent':   'policy-design-site/review-counts',
      },
      body: JSON.stringify({
        query,
        variables: { owner: REPO_OWNER, name: REPO_NAME, categoryId: CATEGORY_ID },
      }),
    });
  } catch (err) {
    console.error('[review-counts] network error:', err.message);
    write({ counts: {}, fetchedAt: null });
    return;
  }

  if (!res.ok) {
    console.error('[review-counts] HTTP', res.status, await res.text());
    write({ counts: {}, fetchedAt: null });
    return;
  }

  const json = await res.json();
  if (json.errors) {
    console.error('[review-counts] GraphQL errors:', JSON.stringify(json.errors));
    write({ counts: {}, fetchedAt: null });
    return;
  }

  const nodes = json.data?.repository?.discussions?.nodes ?? [];
  const counts = {};
  for (const node of nodes) {
    const title = node?.title?.trim();
    if (!title || !title.startsWith(`${TERM_PREFIX}:`)) continue;
    // Title is the giscus "term"; comments.totalCount excludes the discussion
    // body itself, which is what we want (the body is auto-generated by giscus).
    counts[title] = node.comments?.totalCount ?? 0;
  }

  write({ counts, fetchedAt: new Date().toISOString() });
  console.log(`[review-counts] Wrote ${Object.keys(counts).length} thread counts.`);
}

main().catch((err) => {
  console.error('[review-counts] fatal:', err);
  write({ counts: {}, fetchedAt: null });
  process.exitCode = 0; // never block the build
});
